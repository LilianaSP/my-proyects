from math import sqrt

print("El siguiente programa te va a ayudar a obtener el área de unn tetraedro")
triangulo1=float(input("\n introduzca el valor del área de uno de los triángulos del tetraedro en cm: "))
area=((triangulo1**2)*sqrt(3))

print(" \n El área del tetraedro es:", round(area))
